using System;

namespace For_loop_test
{
	class Program
	{
		public static void Main(string[] args)
		{
			
 			for (int counter = 0; counter <= 1000; counter ++)
 			{
 				Console.WriteLine(counter);
 			}
			Console.ReadKey(true);
		}
	}
}